const dupes = ids.filter((id, i) => ids.indexOf(id) !== i);
if (dupes.length > 0) {
  console.warn(`⚠️  Duplicate IDs found: ${[...new Set(dupes)].join(", ")}`);
} else {
  console.log("✅ No duplicate IDs");
}
