const formatted = NEW_SCENES.map((s) => {
  const vibes = JSON.stringify(s.vibes);
  const source = `{ title: ${JSON.stringify(s.source.title)}, year: ${s.source.year} }`;
  return `  {
    id: ${JSON.stringify(s.id)},
    videoId: ${JSON.stringify(s.videoId)},
    start: ${s.start},
    end: ${s.end},
    quote: ${JSON.stringify(s.quote)},
    description: ${JSON.stringify(s.description)},
    vibes: ${vibes},
    era: ${JSON.stringify(s.era)},
    source: ${source},
  }`;
}).join(",\n");

content += `,\n${formatted},\n];\n`;

writeFileSync(scenesPath, content, "utf-8");

